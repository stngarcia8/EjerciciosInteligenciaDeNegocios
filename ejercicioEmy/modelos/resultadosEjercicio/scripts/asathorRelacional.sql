GO

CREATE TABLE dbo.cliente
(
    ID_CLIENTE        BIGINT      IDENTITY(1,1) NOT NULL,
    ID_EMPRESA        INT         DEFAULT 0,
    ID_SEXO           TINYINT     NOT NULL,
    ID_TIPO           TINYINT     NOT NULL,
    NOMBRE_CLIENTE    VARCHAR(20) NOT NULL,
    APELLIDOS_CLIENTE VARCHAR(30) NOT NULL,
    FECNAC_CLIENTE    DATETIME    NOT NULL,
    CONSTRAINT cliente_PK PRIMARY KEY (ID_CLIENTE),
    CONSTRAINT cliente_empresa_FK FOREIGN KEY (ID_EMPRESA)
    REFERENCES dbo.empresa (ID_EMPRESA),
    CONSTRAINT cliente_tipocliente_FK FOREIGN KEY (ID_TIPO)
    REFERENCES dbo.tipo_cliente (ID_TIPO),
    CONSTRAINT cliente_sexo_FK FOREIGN KEY (ID_SEXO)
    REFERENCES dbo.sexo (ID_SEXO)
)
GO

CREATE TABLE dbo.empleado
(
    ID_EMPLEADO     INT         IDENTITY(1,1) NOT NULL,
    ID_SUCURSAL     INT         NOT NULL,
    NOMBRE_EMPLEADO VARCHAR(20) NOT NULL,
    FEC_INGRESOEMP  DATETIME    NOT NULL,
    CONSTRAINT empleado_PK PRIMARY KEY (ID_EMPLEADO),
    CONSTRAINT empleado_sucursal_FK FOREIGN KEY (ID_SUCURSAL)
    REFERENCES dbo.sucursal (ID_SUCURSAL)
)
GO

CREATE TABLE dbo.contrato
(
    ID_CONTRATO    BIGINT       NOT NULL,
    FOLIO_CONTRATO VARCHAR(20)  NOT NULL,
    FEC_CONTRATO   DATETIME     NOT NULL,
    OBS_CONTRATO   VARCHAR(100) DEFAULT '',
    CONSTRAINT contrato_PK PRIMARY KEY (ID_CONTRATO),
)
GO

CREATE TABLE dbo.detalle_contrato
(
    ID_CONTRATO BIGINT NOT NULL,
    ID_PLAN     INT    NOT NULL,
    ID_CELULAR  BIGINT NOT NULL,
    CONSTRAINT detallecontrato_PK PRIMARY KEY ( ID_CONTRATO, ID_PLAN, ID_CELULAR ),
    CONSTRAINT detallecontrato_contrato_FK FOREIGN KEY (ID_CONTRATO)
    REFERENCES dbo.contrato ( ID_CONTRATO ),
    CONSTRAINT detallecontrato_plan_FK FOREIGN KEY (ID_PLAN)
    REFERENCES dbo.plan_telefonia (ID_PLAN),
    CONSTRAINT detallecontrato_celular_FK FOREIGN KEY (ID_CELULAR)
    REFERENCES dbo.celular (ID_CELULAR)
)
GO

CREATE TABLE dbo.venta
(
    ID_VENTA    BIGINT       NOT NULL,
    ID_EMPLEADO INT          NOT NULL,
    ID_CLIENTE  BIGINT       NOT NULL,
    ID_CONTRATO BIGINT       NOT NULL,
    ID_METODO   TINYINT      NOT NULL,
    FEC_VENTA   DATETIME     NOT NULL,
    MONTO_VENTA BIGINT       DEFAULT 0 NOT NULL,
    OBS_VENTA   VARCHAR(100) DEFAULT '',
    CONSTRAINT venta_PK PRIMARY KEY (ID_VENTA),
    CONSTRAINT venta_empleado_FK FOREIGN KEY (ID_EMPLEADO)
   REFERENCES dbo.empleado (ID_EMPLEADO),
    CONSTRAINT venta_cliente_fk FOREIGN KEY (ID_CLIENTE) 
   REFERENCES dbo.cliente (ID_CLIENTE),
    CONSTRAINT    VENTA_CONTRATO_FK FOREIGN KEY (ID_CONTRATO) 
   REFERENCES dbo.contrato (ID_CONTRATO),
    CONSTRAINT venta_metodopago_fk FOREIGN KEY (ID_METODO)
   REFERENCES dbo.metodo_pago (ID_METODO)
)
GO

CREATE TABLE dbo.documento
(
    ID_DOCUMENTO    BIGINT      NOT NULL,
    ID_VENTA        BIGINT      NOT NULL,
    ID_TIPO         TINYINT     NOT NULL,
    ID_ESTADO       TINYINT     NOT NULL,
    FOLIO_DOCUMENTO VARCHAR(20) NOT NULL,
    FEC_EMITIDO     DATETIME    NOT NULL,
    FEC_pago        DATETIME       ,
    MONTO_DOCUMENTO BIGINT      DEFAULT 0 NOT NULL,
    CONSTRAINT documento_PK PRIMARY KEY (ID_DOCUMENTO),
    CONSTRAINT documento_venta_FK FOREIGN KEY (ID_VENTA)
    REFERENCES dbo.venta (ID_VENTA),
    CONSTRAINT documento_estadodocumento_FK FOREIGN KEY (ID_ESTADO)
   REFERENCES dbo.estado_documento (ID_ESTADO),
    CONSTRAINT documento_tipodocumento_FK FOREIGN KEY ( ID_TIPO )
   REFERENCES dbo.tipo_documento ( ID_TIPO )
)
GO
