EXEC dbo.sp_executesql @statement = N'CREATE OR ALTER VIEW [dbo].[vwFactVenta] AS
SELECT 
    CONVERT(INT, CONVERT(DATETIME, vta.FEC_VENTA)) AS ID_FECHA, 
    emp.ID_EMPLEADO, 
    emp.ID_SUCURSAL, 
    vta.ID_CLIENTE, 
    CONVERT(int, CONVERT(varchar, con.ID_CONTRATO) + CONVERT(varchar, det.ID_PLAN)+ CONVERT(varchar, det.ID_CELULAR)) AS ID_CONTRATO, 
    dbo.documento.ID_DOCUMENTO, 
    vta.MONTO_VENTA, 
    CASE 
        WHEN dbo.documento.ID_ESTADo = 1 THEN 1 
        ELSE 0 
    END AS DOC_PAGADO,
    CASE 
        WHEN dbo.documento.ID_ESTADo = 2 THEN 1 
        ELSE 0 
    END AS DOC_VENCIDO, 
    CASE 
        WHEN dbo.documento.ID_ESTADo = 3 THEN 1 
        ELSE 0 
    END AS DOC_PENDIENTE, 
    CASE 
        WHEN dbo.documento.ID_TIPO = 1 THEN 1 
        ELSE 0 
    END AS ES_BOLETA, 
    CASE 
        WHEN dbo.documento.ID_TIPO = 2 THEN 1 
        ELSE 0 
    END AS ES_FACTURA, 
    dbo.documento.MONTO_DOCUMENTO AS MONTO_PAGADO, 
    CASE 
        WHEN dbo.cliente.ID_TIPO = 2 THEN 1 
        ELSE 0 
    END AS CLIENTE_COORPORATIVO, 
    CASE 
        WHEN (
            SELECT COUNT(id_cliente) 
            FROM dbo.venta 
            WHERE id_cliente = vta.id_cliente 
                AND fec_venta < vta.fec_venta) = 0 THEN 1 
        ELSE 0 
    END AS CLIENTE_NUEVO, 
    (SELECT COUNT(ID_CLIENTE) 
    FROM dbo.venta 
    WHERE (ID_CLIENTE = vta.ID_CLIENTE) 
        AND (FEC_VENTA < vta.FEC_VENTA)) AS CANTIDAD_COMPRAS_ANTERIORES, 
    (SELECT COUNT(ID_CONTRATO) 
    FROM dbo.detalle_contrato 
    WHERE (ID_CONTRATO = con.ID_CONTRATO)) AS CANTIDAD_DISPOSITIVOS_CONTRATADOS 
FROM dbo.venta AS vta 
    INNER JOIN dbo.empleado AS emp ON vta.ID_EMPLEADO = emp.ID_EMPLEADO 
    INNER JOIN dbo.contrato AS con ON vta.ID_CONTRATO = con.ID_CONTRATO 
    INNER JOIN dbo.detalle_contrato AS det ON con.ID_CONTRATO = det.ID_CONTRATO 
    INNER JOIN dbo.documento ON vta.ID_VENTA = dbo.documento.ID_VENTA 
    INNER JOIN dbo.cliente ON vta.ID_CLIENTE = dbo.cliente.ID_CLIENTE' 
GO

