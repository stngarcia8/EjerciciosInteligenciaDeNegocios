UPDATE doc 
 SET fec_pago = (
SELECT
    CASE 
    WHEN venta.id_metodo IN (1,2,3) THEN fec_emitido
    WHEN venta.id_metodo = 4 THEN dateadd(day,35, fec_emitido)
    WHEN venta.id_metodo = 5 THEN dateadd(day,65, fec_emitido)
    WHEN venta.id_metodo = 6 THEN dateadd(day,95, fec_emitido)
    END
FROM
    documento
    INNER JOIN venta ON documento.id_venta=venta.id_venta
WHERE documento.id_documento=doc.id_documento)
FROM
    documento doc 
WHERE doc.id_estado=2
