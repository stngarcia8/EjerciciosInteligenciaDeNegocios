CREATE TABLE dbo.tipo_cliente
(
    ID_TIPO      TINYINT     IDENTITY(1,1) NOT NULL,
    TIPO_CLIENTE VARCHAR(60) NOT NULL,
    CONSTRAINT tipo_cliente_PK PRIMARY KEY (ID_TIPO)
)
GO

CREATE TABLE dbo.tipo_documento
(
    ID_TIPO        TINYINT     IDENTITY(1,1) NOT NULL,
    TIPO_DOCUMENTO VARCHAR(60) NOT NULL,
    CONSTRAINT tipo_documento_PK PRIMARY KEY (ID_TIPO)
)
GO

CREATE TABLE dbo.celular
(
    ID_CELULAR       BIGINT      IDENTITY(1,1) NOT NULL,
    ID_MODELO        INT         NOT NULL,
    NRO_CELULAR      VARCHAR(15) NOT NULL,
    fecha_activacion DATE,
