    CONSTRAINT marca_celular_PK PRIMARY KEY (ID_MARCA)
)
GO

CREATE TABLE dbo.metodo_pago
(
    ID_METODO   TINYINT     IDENTITY(1,1) NOT NULL,
    METODO_PAGO VARCHAR(30) NOT NULL,
    CONSTRAINT metodo_pago_PK PRIMARY KEY (ID_METODO)
)
GO

CREATE TABLE dbo.modelo_celular
(
    ID_MODELO INT          NOT NULL,
    ID_MARCA  INT          NOT NULL,
    MODELO    VARCHAR(100) NOT NULL,
    CONSTRAINT modelo_celular_PK PRIMARY KEY (ID_MODELO),
    CONSTRAINT modelo_marca_PK FOREIGN KEY (ID_MARCA)
